  final val inf: Float = Float.PositiveInfinity
  final val eps: Float = 1.0e-5f

  def is(res: Int, exp: Int): Unit =
    assert (res == exp)

  def is_tol(res: Float, exp: Float): Unit = {
    val d = if (res == 0f || res.isInfinite) eps else math.abs(res) * 0.01f
    assert (res >= exp - d && res <= exp + d)
  }

